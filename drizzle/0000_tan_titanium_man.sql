CREATE TABLE IF NOT EXISTS "users" (
	"id" text PRIMARY KEY NOT NULL,
	"name" text NOT NULL,
	"email" text NOT NULL,
	"phone" text,
	"role" "user_enum" DEFAULT 'costumer' NOT NULL,
	"created_at" timestamp DEFAULT now() NOT NULL,
